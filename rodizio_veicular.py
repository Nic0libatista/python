placa = int(input("Digite o número final da placa do veículo: "))

if placa ==1 or placa==2:
    print("Rozidio na Segunda-feira")

elif placa ==3 or placa==4:
    print("Rozidio na Terça-feira")


elif placa ==5 or placa==6:
    print("Rozidio na Quarta-feira")


elif placa ==7 or placa==8:
    print("Rozidio na Quinta-feira")

elif placa ==9 or placa==0:
    print("Rozidio na Sexta-feira")

else:
    print("placa inválida")